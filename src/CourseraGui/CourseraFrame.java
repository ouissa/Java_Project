    public CourseraFrame() throws IOException {
        
        Image frameImage = new ImageIcon("test/courseraLogo.jpg").getImage();
        
        model = new DefaultListModel<>();
        setTitle("Coursera Application");
        setIconImage(frameImage);
        setLayout(new FlowLayout());
        
        Courses = new AssociatedList<Course>();
        
        AssociatedList<Course> Courseso = new AssociatedList<>();
         
        File file = new File("test\\data.ser");
    
             
            AssociatedList<String> lines;
            lines = new AssociatedList<>();
            BufferedReader br = new BufferedReader(new FileReader(file));
            String f = br.readLine();
            while (f != null){
              lines.addElement(f);

              f = br.readLine();       
            }

           
            for(int i = 0; i < lines.elements.size();i++){
                
                    String line = lines.elements.get(i);
                    System.out.println("we are here"+line);
                    String[] results = line.split(";");
                    
                    String title = results[0];
                    double fees = Double.parseDouble(results[1]);
                    String reference = results[3];
                    String description = results[2];
                    String cert = results[4];
                    
                    Course course = new Course(reference,title,description,fees,cert);
                
                    Courseso.addElement(course);
              
                
                      
            }
            br.close();
            this.Courses = Courseso;
            System.out.println("Yaaa rebi"+Courses);
        
        
        
 

        initComponents();
    }
    
