    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        // open the file and do modifications
        
        
         try (FileWriter writer = new FileWriter("test/data.ser");
             BufferedWriter bw = new BufferedWriter(writer)) {
             String Content = "" ;
            ListIterator<Course> iter = Courses.elements.listIterator();
        while(iter.hasNext()){
            
            Course cou = iter.next();
            Content += cou.getTitle()+";"+cou.getFees()+";"+cou.getDescription()+";"+cou.getReference()+";"+cou.getCertificate();
            bw.write(Content);
            Content = "";
            if(iter.hasNext()){
                bw.write("\n");
            }
            
        }
            

        } catch (IOException e) {
            System.err.format("IOException: %s%n", e);
        }        
    }//GEN-LAST:event_saveActionPerformed

