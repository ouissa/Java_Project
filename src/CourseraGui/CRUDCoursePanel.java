    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        
        //add course String 
        String ref = refTxt.getText();
        String title = titleTxt.getText();
        String desc = descTxt.getText();
        String feesStr = fees.getText();
        String sessionStr = SessionTime.getText();
        double fees = 0;
        int flag = 1;
        try{
            
            fees = Double.parseDouble(feesStr);
            
        }catch(NumberFormatException e){
            flag = 0;
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" + "Expected a double","Adding a course",
                    JOptionPane.ERROR_MESSAGE);
        }
 
        if(ref.isEmpty()){
            flag = 0;
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" +"Reference Field is Empty","Adding a course",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        if(desc.isEmpty()){
            flag = 0;
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" + "Description Field is Empty","Adding a course",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        if(title.isEmpty()){
            flag = 0;
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" +"Title Field is Empty","Adding a course",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        if(sessionStr.isEmpty()){
            flag = 0;
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" + "Session Time filed is empty","Adding a course",
                    JOptionPane.ERROR_MESSAGE);
        }
       Certificate certi = (Certificate)jComboBox1.getSelectedItem();
       String certo = certi.toString();
        
        //create course objectt
        if(flag == 1){
            Course course = new Course(ref,title,desc,fees,certo);
            Courses.addElement(course);
            JOptionPane.showMessageDialog(CRUDCoursePanel.this,"" + "Course Added Successfully","Adding a course",
                    JOptionPane.INFORMATION_MESSAGE);
        
            model.addElement(course);
            CourseList.setSelectedValue(course,true);
        }
        
        
        
        
    }//GEN-LAST:event_addButtonActionPerformed

