    private void CourseListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_CourseListValueChanged
        // TODO add your handling code here:
        if(evt.getValueIsAdjusting()){
            return;
        }
        
        Course courso = CourseList.getSelectedValue();
        
        
        if(courso == null){return;}
        
        refTxt.setText(courso.getReference());
        SessionTime.setText(courso.getSessionTime());
        descTxt.setText(courso.getDescription());
        titleTxt.setText(courso.getTitle());
        fees.setText(String.valueOf(courso.getFees()));
        jComboBox1.setSelectedItem(Certificate.valueOf(courso.getCertificate()));
       
        

       
        
       
    }//GEN-LAST:event_CourseListValueChanged

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        Course course = CourseList.getSelectedValue();
        course.setReference(refTxt.getText());
        course.setDescription(descTxt.getText());
        course.setTitle(titleTxt.getText());
       
        course.setCertificate(jComboBox1.getSelectedItem().toString());
     
        
  
        course.setFees(Double.parseDouble(fees.getText()));
        //update the sessionTime and the certificate field also 
        
    }//GEN-LAST:event_editButtonActionPerformed

